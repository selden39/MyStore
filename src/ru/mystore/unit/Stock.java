package ru.mystore.unit;

public class Stock {
    private String name;
    private String address;

    public Stock(String name, String address) {
        this.name = name;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }


}
